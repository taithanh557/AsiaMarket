// Helper function to get flash sale products
export const flashSaleProducts = products.filter(product => product.isFlashSale);

// Helper function to get popular products (based on sold count)
export const popularProducts = [...products].sort((a, b) => b.sold - a.sold);

// Helper function to get a product by ID
export const getProductById = (id: number) => {
  return products.find(product => product.id === id);
};

// Helper function to get related products (same category, excluding the current product)
export const getRelatedProducts = (id: number) => {
  const product = getProductById(id);
  if (!product) return [];
  
  return products
    .filter(p => p.category === product.category && p.id !== id)
    .slice(0, 5);
};

